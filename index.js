function checkIfCommitToday() {
  daysSinceStart = Math.floor(
    DateTime.now().diff(DateTime.local(startYear, startMonth, startDay), "days")
      .days
  );
  if (daysSinceStart >= maxDays) {
    daysSinceStart = daysSinceStart % maxDays;
  }
  let y = Math.floor(daysSinceStart / 7);
  let x = daysSinceStart % 7;
  return Boolean(matrix[x][y]);
}

